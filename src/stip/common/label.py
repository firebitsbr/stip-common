def sanitize_id(word):
    RESERVED_CHARS = {
        '-': '--2d',
        '.': '--23',
        ' ': '--20',
        '\'': '--27',
        '"': '--22',
        '(': '--28',
        ')': '--29',
        ':': '--3a',
        '/': '--2f',
        '?': '--3f',
        '=': '--3d',
        '[': '--5b',
        ']': '--5d',
        '{': '--7b',
        '}': '--7d',
        '#': '--23',
        '@': '--40',
        '!': '--21',
        '$': '--24',
        '&': '--26',
        '+': '--2b',
        ',': '--2c',
        ';': '--3b',
        '%': '--25',
    }
    ret = ''
    for c in word:
        if c in RESERVED_CHARS:
            ret += ('%s' % (RESERVED_CHARS[c]))
        else:
            ret += c
    return ret